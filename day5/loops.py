'''Notes on for loops'''
# Loops allow us to execute code multiple times.
# For loops interact with lists in a neat way.
fruits = ["Apple", "Peach", "Pear"]

# This for loop is taking each item in the list and assigning the variable "fruit" to each.
# Each time the for loop cycles it assigns fruit to the next value in the list.
for fruit in fruits:
    print(fruit)
    print(fruit + " pie")
