'''Notes on Datatypes in Python'''
# Pythons primitive data types are strings, integers, floats, and booleans
# Most numbers start at 0 in programming.

# Subscripting here will print the 4th character in the string (starting with 0).
# This lets you pull out individual characters from a string.
print("hello"[4])
# Subscripting can also deal with negatives as well.
print("hello"[-1])

# Integer - whole numbers with no decimals. Underscores "234,434"
# can be used with underscores "234_434"
print(123 + 345)

# Float - Numbers with decimals, floating point number
print(3.14159)

# Boolean - True or False, always begin with capitals.
print(True)
print(False)
