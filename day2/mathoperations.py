'''Notes on Mathematical operations in python'''

# Python allows for various types of math.
# + for addition
# - for subtraction,
# * for multiplication,
# / for division (always makes floating point) (Also known as implicit type casting)
# // for division (creates an integer) (Note, just cuts out remainder)
# % for the Modulus, which is just the remainder.
# ** for exponents (2^2 or 2*2)
# Mathematical operation follows PEMDAS.
# () , **, * or /, + or -

print(2 ** 3)

print(3 * (3 + 3) / 3 - 3)
