'''Notes on While Loops'''

# A while loop is a loop similar to a for loop. Where with a for loop,
# we can have it set to either  range (for number in range (a, b))
# or for a list (for item in list_of_items), a while loop will run while
# something is true or false

# For loops are great for iteration over multiple things (such as a list)
# Or for a range of items.
# A while loop is useful when you don't care what item you're at in the list
# When you want something to loop until you meet a condition that you've set.
# For loops are traditionally "safer", since a while loop can easily become infinite.

NEZZY = True
while NEZZY is True: # This can also be an equality, or a check of a string.
    print("Nezzy is cute")
